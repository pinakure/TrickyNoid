function HitReaction(){
	this.causeBounce = false;
    this.invertDeltas = false;					//If true, ball deltas swap
    this.causeExplosion = false;				//If true, hit brick explodes  
    this.isExplosion = false;					
    this.causeFall = false;					//If true, hit brick begins to fall
    this.causeSelectiveDestruction = false;	//If true, bricks with same id as hit brick dissappear
	this.target = false;
}








